#include <stdio.h>
#include <bits/stdc++.h>

using namespace std;
const int N = 1000005;

string s,t;
int l,L,p,P;

void init(){
	while(cin >> s >> t){
		l = s.length();
		L = t.length();
		p = P = 0;
		while(p < l){
			while(P < L && (s[p] != t[P])) P++;
			if(P==L) break;
			p++, P++; 
		}
		if(p==l) printf("Yes\n");
		else printf("No\n");
	}
}

