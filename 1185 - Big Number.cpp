#include <stdio.h>
#include <bits/stdc++.h>

using namespace std;
const int N = 10000005;

double lg[N];
int n;

void init(){
    lg[0] = 0;
    for(int i = 1; i < N; i++){
        lg[i] = lg[i-1] + log10(i);
    }
    int T;
    scanf("%d",&T);
    for(int t=1; t<=T; t++){
        scanf("%d",&n);
        int res = lg[n] + 1;
        printf("%d\n",res);
    }
}

