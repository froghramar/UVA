#include <stdio.h>
#include <bits/stdc++.h>

using namespace std;
const int N = 1000005;

char l[2][33], s[100];
bool shift;
int mask;

void init(){
    gets(l[0]);
    gets(l[1]);
    while(scanf("%s",s) == 1){
        shift = 0;
        for(int i = 0; s[i] ; i++){
            mask = mask * 2 + (s[i] - '0');
            if((i + 1) % 5) continue;
            if(mask == 27) shift = 0;
            else if(mask == 31) shift = 1;
            else putchar(l[shift][mask]);
            mask = 0;
        }
        puts("");
    }
}

